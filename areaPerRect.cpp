// This program calculates the area and perimeter of a 2cm x 10cm rectangle.

#include <iostream>

int main() {
    std::cout << "For a rectangle with a length of 2cm and width of 10cm: \n";
    std::cout << "\n";
    std::cout << "Area =  " << 2 * 10 << " cm^2\n";
    std::cout << "Perimeter =  " << 2 * (2 + 10) << " cm\n";
}
